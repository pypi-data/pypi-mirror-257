class Addition:
    """
    Instantiate a addition operation.
    Numbers will be added.
    """

    def addition(self, num1, num2):
        """
        Add num1 with num2.

        :param num1: First Number.
        :type number: float

        :param num2: Second Number.
        :type number: float

        :return: The result of the addition.
        :rtype: float
        """

        return num1 + num2

    def product(self, num1, num2):
        """
        Multiply num1 with num2.
        
        :param num1: First Number.
        :type number: float

        :param num2: Second Number.
        :type number: float

        :return: The result of the multiplication.
        :rtype: float
        """
        
        return num1 * num2
    
    def substraction(self, num1, num2):
        """
        substract num2 from num1.
        
        :param num1: First Number.
        :type number: float

        :param num2: Second Number.
        :type number: float

        :return: The result of the multiplication.
        :rtype: float
        """
        
        return num1 - num2
    